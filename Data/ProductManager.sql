CREATE DATABASE ProductManager
GO


USE ProductManager

CREATE TABLE Product (
Id INT IDENTITY,
ArticleNumber NVARCHAR(10) NOT NULL,
Name NVARCHAR(50) NOT NULL,
Description NVARCHAR(200) NOT NULL,
Price DECIMAL (18,2)
PRIMARY KEY (Id)

)

CREATE TABLE Category (
Id INT IDENTITY,
Name NVARCHAR (100) NOT NULL
PRIMARY KEY (Id)


)